print("For AND Operator")     # multiply wala
print(True and True)
print(True and False)
print(False and True)
print(False and False)

print("\n For OR Operator")   # addition wala
print(True or True)
print(True or False)
print(False or True)
print(False or False)

print("\n NOT Operator")      # opposite wala
print(not(True)) 
print(not(False))