c = True 
d = False

print("For AND Operator")     # multiply wala
print(c and c)
print(c and d)
print(d and c)
print(d and d)

print("\n For OR Operator")   # addition wala
print(c or c)
print(c or d)
print(d or c)
print(d or d)

print("\n NOT Operator")      # opposite wala
print(not(c)) 
print(not(d))