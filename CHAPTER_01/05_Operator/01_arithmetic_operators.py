a = 34

b = 3

# Arithmetic Operator 
print("a + b = ", a + b)        # sum
print("a - b = ", a - b)        # substraction
print("a * b = ", a * b)        # multiply
print("a / b = ", a / b)        # division which give decimal
print("a % b = ", a % b)        # modulus
print("a // b = ", a // b)      # division which give integer
print("a ** b = ", a ** b)      # a to the power b